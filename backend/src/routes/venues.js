/**
 *  @swagger
 *  /api/venues:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Venues]
 *      summary: Get all venues
 *      description: Get all venues
 *      responses:
 *        200:
 *          description: Venues list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Venues"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */
router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const payload = await VenuesDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'name',
        'location',
        'equipment',
        'event_types',
        'Venue_Type',
        'Address',
        'About',
        'capacity',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

